const config = useRuntimeConfig()
const apiBase = config.public.apiBase

// State
const stats = ref({
  totalNovels: 0,
  totalChapters: 0,
  totalUsers: 0,
  totalViews: 0
})

const recentActivities = ref([])
const loading = ref(false)

// Fetch dashboard data
const fetchDashboardData = async () => {
  try {
    loading.value = true
    
    // Fetch stats (you'll need to create these endpoints in backend)
    const statsResponse = await apiGet(`${apiBase}/api/v1/admin/stats`)
    stats.value = statsResponse
    
    // Fetch recent activities
    const activitiesResponse = await apiGet(`${apiBase}/api/v1/admin/activities`)
    recentActivities.value = activitiesResponse.activities || []
    
  } catch (error) {
    console.error('Error fetching dashboard data:', error)
  } finally {
    loading.value = false
  }
}

// Format date
const formatDate = (dateString) => {
  return new Date(dateString).toLocaleDateString('vi-VN', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  })
}

onMounted(async () => {
  await fetchDashboardData()
})
